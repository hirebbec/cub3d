#include "libft.h"

int	ft_atoi(const char *str)
{
	int	res;
	int	negative;

	negative = 1;
	res = 0;
	while (*str == '\t' || *str == '\r' || *str == '\n'
		|| *str == ' ' || *str == '\v' || *str == '\f')
		str++;
	if (*str == '-')
	{
		negative *= -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - '0';
		str++;
	}
	return (res * negative);
}
