char	*ft_strchr(const char *s, int c)
{
	char	sym;

	sym = c;
	while (*s)
	{
		if (*s == sym)
			return ((char *)s);
		s++;
	}
	if (sym == '\0')
		return ((char *)s);
	return (0);
}
